/*******************************************************************************
  SCRIPT DE ESTUDO: FULL-TEXT SEARCH (FTS) NO INNODB
  PADRONIZAÇÃO: VÍRGULAS À DIREITA E CÁLCULO DE RELEVÂNCIA (SCORE).
*******************************************************************************/

-- =============================================================================
-- 1. CONFIGURAÇÃO DA TABELA COM ÍNDICE FULLTEXT
-- O MOTOR INNODB SUPORTA ÍNDICES FULLTEXT PARA BUSCAS COMPLEXAS EM TEXTO.
-- =============================================================================

USE CURSO;

CREATE TABLE IF NOT EXISTS ARTIGO (
      ID INT AUTO_INCREMENT NOT NULL PRIMARY KEY,
      TITULO VARCHAR(200),
      CORPO TEXT,
      FULLTEXT(TITULO, CORPO) -- CRIA O ÍNDICE PARA BUSCA OTIMIZADA
) ENGINE=INNODB;

-- INSERÇÃO DE DADOS PARA GERAR O RESULTADO DA BASE DE CONHECIMENTO
INSERT INTO ARTIGO (TITULO, CORPO) VALUES
      ('MYSQL TUTORIAL', 'SGBD MYSQL DO ZERO...'),
      ('COMO UTILIZAR BEM O MYSQL', 'DEPOIS QUE ANALISAMOS MUITO...'),
      ('OTIMIZANDO MYSQL', 'NESSE TUTORIAL VAMOS APRENDER...'),
      ('1001 DICAS DE MYSQL', '1. NUNCA INICIE O MYSQL COM ROOT. 2...'),
      ('MYSQL VS SQL SERVER', 'NESTA COMPARAÇÃO VAMOS...'),
      ('SEGURANÇA NO MYSQL', 'QUANDO CONFIGURADO CORRETAMENTE, O MYSQL...');


-- =============================================================================
-- 2. BUSCA EM MODO DE LINGUAGEM NATURAL (NATURAL LANGUAGE MODE)
-- RETORNA OS REGISTROS QUE CONTÉM A PALAVRA-CHAVE COM BASE NA RELEVÂNCIA.
-- =============================================================================

SET @PESQUISA := 'TUTORIAL';

-- RETORNA AS LINHAS QUE COINCIDEM COM O TERMO PESQUISADO
SELECT * FROM ARTIGO
	WHERE MATCH (TITULO, CORPO) AGAINST 
	(@PESQUISA IN NATURAL LANGUAGE MODE);

-- GERA O RESULTADO DA CONTAGEM DE OCORRÊNCIAS
SELECT COUNT(*) FROM ARTIGO

WHERE MATCH (TITULO, CORPO) AGAINST 
(@PESQUISA IN NATURAL LANGUAGE MODE);


-- =============================================================================
-- 3. CÁLCULO DE SCORE (RELEVÂNCIA)
-- O RESULTADO DO SCORE INDICA O QUÃO PRÓXIMO O TEXTO ESTÁ DA PESQUISA.
-- =============================================================================

SET @PESQUISA := 'TUTORIAL OTIMIZANDO';

-- RETORNA O ID E O VALOR DECIMAL DA RELEVÂNCIA PARA CADA LINHA
SELECT ID, MATCH(TITULO, CORPO) AGAINST
(@PESQUISA IN NATURAL LANGUAGE MODE) AS SCORE
FROM ARTIGO;


-- =============================================================================
-- 4. PESQUISA POR SENTENÇAS E PONDERAÇÃO
-- O MYSQL IGNORA PALAVRAS MUITO COMUNS (STOPWORDS) DEPENDENDO DA CONFIGURAÇÃO.
-- =============================================================================

SET @PESQUISA := 'QUANDO CONFIGURADO CORRETAMENTE, O MYSQL';

-- RETORNA APENAS OS RESULTADOS COM RELEVÂNCIA POSITIVA
SELECT ID,TITULO,CORPO,
       MATCH(TITULO, CORPO) AGAINST 
       (@PESQUISA IN NATURAL LANGUAGE MODE) AS SCORE
FROM ARTIGO 
WHERE MATCH(TITULO, CORPO) AGAINST 
(@PESQUISA IN NATURAL LANGUAGE MODE);