-- CRIAÇÃO DA VIEW QUE UNIFICA OS RESULTADOS DAS QUATRO TABELAS
CREATE OR REPLACE VIEW V_FORNEC_GERAL AS
SELECT 'F1' AS ORIGEM, ID_FORNEC, FORNEC FROM FORNEC1
UNION ALL
SELECT 'F2' AS ORIGEM, ID_FORNEC, FORNEC FROM FORNEC2
UNION ALL
SELECT 'F3' AS ORIGEM, ID_FORNEC, FORNEC FROM FORNEC3
UNION ALL
SELECT 'F4' AS ORIGEM, ID_FORNEC, FORNEC FROM FORNEC4;

-- RETORNO COMPLETO DOS FORNECEDORES COMO SE FOSSEM UMA ÚNICA TABELA
SELECT * FROM V_FORNEC_GERAL ORDER BY ID_FORNEC;