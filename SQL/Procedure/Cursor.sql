/*******************************************************************************
  SCRIPT DE ESTUDO: CURSORES E CLASSIFICAÇÃO DINÂMICA
  PADRONIZAÇÃO: VÍRGULAS À DIREITA E PROCESSAMENTO LINHA A LINHA.
*******************************************************************************/

DELIMITER $$

CREATE PROCEDURE PROC_CAMPEONATO (INOUT CLASSIFICACAO VARCHAR(400))
BEGIN
      -- DECLARAÇÃO DE VARIÁVEIS DE CONTROLE DE FLUXO E RESULTADO
      DECLARE P_FINAL   INTEGER DEFAULT 0;
      DECLARE P_TIME    VARCHAR(100) DEFAULT "";
      DECLARE P_PONTOS  INTEGER DEFAULT 0;
      DECLARE P_POSICAO INTEGER DEFAULT 1;
      DECLARE P_ACUM    INTEGER DEFAULT 0;
      DECLARE P_SITUA   VARCHAR(100);

      -- 1. DEFINIÇÃO DO CURSOR
      -- SELECIONA OS TIMES PARA O RETORNO ORDENADO POR PONTOS (DESC)
      DECLARE TIME_CURSOR CURSOR FOR 
            SELECT NOMETIME, PONTOS FROM CAMPEONATO ORDER BY PONTOS DESC;

      -- 2. HANDLER DE FIM DE CURSOR
      -- QUANDO NÃO HOUVER MAIS LINHAS (NOT FOUND), DEFINE A VARIÁVEL PARA SAÍDA
      DECLARE CONTINUE HANDLER FOR NOT FOUND SET P_FINAL = 1;

      -- CRIAÇÃO DA ESTRUTURA TEMPORÁRIA PARA O RESULTADO CONSOLIDADO
      CREATE TEMPORARY TABLE LISTA_CAMP 
      (
            POSICAO  INT,
            TIME     VARCHAR(100),
            PONTOS   INT,
            P_ACUM   INT,
            SITUACAO VARCHAR(100)
      );

      -- 3. ABERTURA E ITERAÇÃO DO CURSOR
      OPEN TIME_CURSOR;

      LOOP_CAMPEONATO: LOOP
            
            -- EXTRAI A PRÓXIMA LINHA PARA AS VARIÁVEIS LOCAIS
            FETCH TIME_CURSOR INTO P_TIME, P_PONTOS;

            -- VERIFICA SE O HANDLER SINALIZOU O FIM DOS DADOS
            IF P_FINAL = 1 THEN
                  LEAVE LOOP_CAMPEONATO;
            END IF;

            -- LÓGICA DE NEGÓCIO: CLASSIFICAÇÃO POR POSIÇÃO (G4, Z4, ETC)
            IF P_POSICAO <= 6 THEN
                  SET P_SITUA = 'LIBERTADORES';
            ELSEIF P_POSICAO <= 8 THEN
                  SET P_SITUA = 'PRÉ-LIBERTADORES';
            ELSEIF P_POSICAO <= 12 THEN
                  SET P_SITUA = 'SUL-AMERICANA';
            ELSEIF P_POSICAO <= 16 THEN
                  SET P_SITUA = 'ZONA NEUTRA';
            ELSE
                  SET P_SITUA = 'REBAIXADO';
            END IF;

            -- ACUMULA O SOMATÓRIO PARA O RESULTADO DE PERFORMANCE
            SET P_ACUM = P_ACUM + P_PONTOS;
            
            -- PERSISTE O PROCESSAMENTO NA TABELA TEMPORÁRIA
            INSERT INTO LISTA_CAMP (POSICAO, TIME, PONTOS, P_ACUM, SITUACAO)
            VALUES (P_POSICAO, P_TIME, P_PONTOS, P_ACUM, P_SITUA);

            -- INCREMENTA O CONTADOR DE CLASSIFICAÇÃO
            SET P_POSICAO = P_POSICAO + 1;

      END LOOP LOOP_CAMPEONATO;

      -- 4. FECHAMENTO DO CURSOR PARA LIBERAR RECURSOS
      CLOSE TIME_CURSOR;

      -- RETORNA A TABELA DE CLASSIFICAÇÃO FINAL
      SELECT * FROM LISTA_CAMP;

      -- LIMPEZA DOS OBJETOS TEMPORÁRIOS
      DROP TEMPORARY TABLE IF EXISTS LISTA_CAMP;

END $$

DELIMITER ;

-- =============================================================================
-- EXECUÇÃO E TESTE DO RETORNO
-- =============================================================================

SET @CLASSIFICACAO = "";
CALL PROC_CAMPEONATO(@CLASSIFICACAO);

-- CONSULTA O VALOR DA VARIÁVEL CASO ELA TENHA SIDO ATUALIZADA NA PROCEDURE
SELECT @CLASSIFICACAO AS RESUMO_FINAL;